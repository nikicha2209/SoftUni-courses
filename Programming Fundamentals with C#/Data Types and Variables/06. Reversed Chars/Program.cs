using System;

namespace _06._Reversed_Chars
{
    internal class Program
    {
        static void Main(string[] args)
        {
            char a = char.Parse(Console.ReadLine());
            char b = char.Parse(Console.ReadLine()); 
            char c = char.Parse(Console.ReadLine());
            Console.Write(c + " " );
            Console.Write(b + " " );
            Console.Write(a);
        }
    }
}
